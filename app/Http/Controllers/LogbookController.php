    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $input = $request->all();
        $input['logbook_title'] = $input['logbook_title'];
        $input['logbook_body'] = $input['logbook_body'];
        $input['customer_id'] = $input['customer_id'];

        $dom = new \DOMDocument();
        $dom->loadHtml('<meta http-equiv="content-type" content="text/html; charset=utf-8">'.$input['logbook_body']);
        $images = $dom->getElementsByTagName('img');

        foreach($images as $img){
            $src = $img->getAttribute('src');

            // if the img source is 'data-url'
            if(preg_match('/data:image/', $src)){
                // get the mimetype
                preg_match('/data:image\/(?<mime>.*?)\;/', $src, $groups);
                $mimetype = $groups['mime'];
                // Generating a random filename
                $filename = uniqid();
                $filepath = "/images/$filename.$mimetype";
                // @see http://image.intervention.io/api/
                $image = Image::make($src)
                    // resize if required
                    /* ->resize(300, 200) */
                    ->encode($mimetype, 100)  // encode file to the specified mimetype
                    ->save(public_path($filepath));
                $new_src = asset($filepath);
                $img->removeAttribute('src');
                $img->setAttribute('src', $new_src);
            } // <!--endif
        } // <!-
        $input['logbook_body'] = $dom->saveHTML();
        Logbook::create($input);
        Session::flash('flash_message','Logbook Successfully Added.');
        Session::flash('flash_type','success');
        return redirect("customers/".$input['customer_id']."");

    }

